package labBook;

public class TypeCasting {
     public static void main (String[]args) {
    	 byte b=10;
    	 int i=b;
    	 System.out.println(i);
    	 int cx=100;
    	 
    	 byte d = (byte)cx;
    	 System.out.println(d);
    	 
    	 byte q=10;
    	 byte w=10;
    	 int sum = q*w;
    	 System.out.println(sum);
    	 
    	 
    	 
     }

}
