package runall;


public class Lab8 {
	public static void main(String[] args) {
			
		char c='e';
		switch(c) {
		case 'a':{
			System.out.println("the character \"a\" is a vowel");
			break;
		}
		case 'e':{
			System.out.println("the character \"e\" is a vowel");
			break;
		}
		case 'i':{
			System.out.println("the character \"i\" is a vowel");
			break;
		}
		case 'o':{
			System.out.println("the character \"o\" is a vowel");
			break;
		}
		case 'u':{
			System.out.println("the character \"u\" is a vowel");
			break;
		}
		default:{
			System.out.println("Not a vowel...");
		}
		}
	}
}
