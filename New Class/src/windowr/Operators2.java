package windowr;

public class Operators2 {
      public static void main(String[] args) {
	   
    	  int i=10;
    	  System.out.println(i++);
    	  System.out.println(i);
    	  
    	  i=5;
    	  System.out.println(++i);
    	  
    	  i=25;
    	  System.out.println(i--);
    	  System.out.println(i--);
    	  System.out.println(i--);
    	  System.out.println(i);
    	  
    	  i=50;
    	  System.out.println(--i);
    	  System.out.println(--i);
    	  System.out.println(--i);
    	  
    	  i=20;
    	  i+=4;
    	  System.out.println(i);
    	  i=5;
    	  i-=4;
    	  System.out.println(i);
    	  
    	  
    	  i=5;
    	  System.out.println(i==5);
    	  System.out.println(i!=1);
	
      }
}
