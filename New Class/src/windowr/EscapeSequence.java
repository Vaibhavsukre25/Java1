package windowr;

public class EscapeSequence {
	
	public static void main(String[] args) {
		
		System.out.println("\"Vaibhav Sukre\"");
		System.out.println("\'Vaibhav Sukre\'");
		System.out.println("Vaibhav\n \nSukre");
		System.out.println("Vaibhav \t Sukre");
		System.out.println("Vaibhav Vaibhav \r\r Sukre");
		System.out.println("Vaibhav\b\b\bSukre");
	}
}
